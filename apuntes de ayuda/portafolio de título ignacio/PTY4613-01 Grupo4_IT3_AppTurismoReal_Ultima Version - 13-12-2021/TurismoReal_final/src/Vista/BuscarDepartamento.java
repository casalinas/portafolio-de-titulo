       public void iconocambio(String n_departamento) {
        Conexion con = new Conexion();
        Connection coni = con.getConexion();
        tabla = new DefaultTableModel(null, columnas);
        

        try {

            String sql = "SELECT departamento, n_departamento, valor, estado, nombre FROM departamentos INNER JOIN estado ON departamentos.estado_id_estado = estado.id_estado INNER JOIN edificio ON edificio.id_edificio=departamentos.edificio_id_edificio WHERE n_departamento LIKE '" + n_departamento + "%' OR departamento LIKE '"+n_departamento + "%'  OR estado LIKE '"+n_departamento + "%'  OR nombre LIKE '"+n_departamento + "%'";
        
            ps = coni.prepareStatement(sql);
            rs=ps.executeQuery();
            if (rs.next()) {
                lblCheck.setIcon(verde);
                lblBusca.setText("Resultados Posibles");
            }else{
                                lblCheck.setIcon(rojo);
                lblBusca.setText("Sin resultado");
            }
            con.desconectar();
        } catch (Exception e) {
            
            System.out.println("Error: "+e.getMessage());
        }
    }
       
