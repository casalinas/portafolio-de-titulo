    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        UsuariosDAO dao = new UsuariosDAO();
        Usuarios u = new Usuarios();
        int r = 0;
        int rep = 0;

            try {

                u.setNombre(txtNombre.getText());
                u.setRut(txtRut.getText());
                u.setAppat(txtAppt.getText());
                u.setApmat(txtApmt.getText());
                u.setTelefono(txtTelefono.getText());
                u.setEmail(txtEmail.getText());
                u.setPassword(txtContraseña.getText());
                u.setR(rep);

                if (dao.newinsertar(u)) {
                    JOptionPane.showMessageDialog(null, "Usuario Agregado correctamente!");
                    this.dispose();
                    IndexAdmin l = new IndexAdmin();
                    l.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario no agregado");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "error: " + e.getMessage());
            }

        
    }//GEN-LAST:event_btnAgregarActionPerformed

