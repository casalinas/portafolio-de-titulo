    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
  Edificio u = new Edificio();
        int r = 0;
        int rep = 0;
        int id = cboCity.getItemAt(cboCity.getSelectedIndex()).getId();
        if (txtNombre.getText().trim().length() == 0 || txtDireccion.getText().trim().length() == 0) {
           JOptionPane.showMessageDialog(null, "Falta llenar campos de texto");
        }else{
        
         try {

            u.setNombre(txtNombre.getText());
            u.setDireccion(txtDireccion.getText());
            u.setId_ciudad(id);

            if (modedificio.AgregarEdificio(u)) {
                JOptionPane.showMessageDialog(null, "Edificio Agregado");
                AgregarDepartamento a=new AgregarDepartamento();
                this.dispose();
                a.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Edificio No agregado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error: " + e.getMessage());
        }
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

