    public void validar() {
        if (txtApmt.getText().isEmpty() || txtAppt.getText().isEmpty() || txtContraseña.getText().isEmpty() ||
                txtEmail.getText().isEmpty() || txtNombre.getText().isEmpty() || txtRut.getText().isEmpty() ||
                txtTelefono.getText().isEmpty()) {
            btnAgregar.setEnabled(false);
            
        }else{
           btnAgregar.setEnabled(true);
        }
    }


