    public void cargarDatosTabla() {
        Conexion con = new Conexion();
        Connection coni = con.getConexion();
        tabla = new DefaultTableModel(null, columnas);
        

        try {

            String sql = "SELECT departamento, n_departamento, valor, estado, nombre FROM departamentos INNER JOIN edificio ON edificio.id_edificio=departamentos.edificio_id_edificio INNER JOIN estado ON departamentos.estado_id_estado = estado.id_estado WHERE estado_id_estado!=4";
        
            ps = coni.prepareStatement(sql);
            rs=ps.executeQuery();
            while(rs.next()){
                filas[0] = rs.getString(1);
                filas[1] = rs.getString(2);
                filas[2] = rs.getString(3);
                filas[3] = rs.getString(4);
                filas[4] = rs.getString(5);
                tabla.addRow(filas);
            }
            
            tblDatos.setModel(tabla);
            con.desconectar();
        } catch (Exception e) {
            
            System.out.println("Error: "+e.getMessage());
        }
    }
    
