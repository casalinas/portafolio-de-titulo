    private void llenarCBEstado() {
        ArrayList<Estado> listaEstado = modedificio.getListaEstado();
        cboEstado.removeAllItems();
        for (int i = 0; i < listaEstado.size(); i++) {
            cboEstado.addItem(new Estado(listaEstado.get(i).getId_estado(), listaEstado.get(i).getEstado()));
        }

    }

    public void busquedad_dep() {
        String n_departamento = txtBuscarId.getText();
        String mensaje = modepartamento.buscarNro2(n_departamento);

        if (mensaje.equals("Departamento encontrado")) {
            lblMensaje.setText("<html><center>Numero encontrado, se puede actualizar el departamento");
            lblImagen.setIcon(verde);
            desbloquear();

        } else {
            lblMensaje.setText("<html><center>Numero no encontrado");
            lblImagen.setIcon(rojo);
            bloquear();
            borrar();

        }

        if (txtBuscarId.getText().isEmpty()) {
            lblMensaje.setText("");
            lblImagen.setIcon(null);
        }
    }

    public void borrar() {

       cboEstado.setSelectedIndex(0);
        bloquear();
    }

    public void borrar2() {
        txtBuscarId.setText("");
        lblMensaje.setText("");
        lblImagen.setIcon(null);
    }

    public void bloquear() {
        for (Component a : pnlMod.getComponents()) {
            a.setEnabled(false);
        }

        for (Component b : pnlOpciones.getComponents()) {
            b.setEnabled(false);
        }
    }

    public void desbloquear() {
        for (Component a : pnlMod.getComponents()) {
            a.setEnabled(true);
        }

        for (Component b : pnlOpciones.getComponents()) {
            b.setEnabled(true);
        }
    }

    public void modificarDep() {

        try {
            int id = cboEstado.getItemAt(cboEstado.getSelectedIndex()).getId_estado();
            d.setId_Estado(id);
            d.setN_valido(txtBuscarId.getText());
            modepartamento.modificarDisponibilidad(d);
            borrar();
            borrar2();
        } catch (Exception e) {
        }
    }

